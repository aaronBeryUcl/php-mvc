		<h2>Colour palette tints</h2>
		
		<p>There are a variety of colours achievable by using tints of the UCL colour palette. The tints can be achieved on the website by using the following Sass method.</p>
		
		<p><code>
		background: darken($colorname, 30%);
		</code></p>
		
		<p>or</p>
		
		<p><code>
			background: lighten($colorname, 30%);
		</code></p>
		
